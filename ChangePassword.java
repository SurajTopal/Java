    private void changepasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changepasswordActionPerformed
            // TODO add your handling code here:
            String oldpasswordtxt=this.oldpasswordtxt.getText();
            String newpasswordtxt=this.newpasswordtxt.getText();
            String passwordtxt=this.passwordtxt;
            if(oldpasswordtxt.equals("") || newpasswordtxt.equals(""))
            {
                warning.setText("Empty Field..");
                   warning.setForeground(Color.red);
                   warning.setVisible(true);
            }
            else
            {
                if(oldpasswordtxt.equals(passwordtxt))
                {
                   if(newpasswordtxt.length()>8)
                   {
                       if(!newpasswordtxt.equals(passwordtxt))
                       {
                           database(emailtxt,newpasswordtxt);
                            JOptionPane.showMessageDialog(ChangePassword.this,"Password Changed.."); 
                           //warning.setForeground(Color.green);
                           warning.setVisible(false);
                           close();
                       }
                       else
                       {
                                 warning.setText("you cannot write the old password..");
                                 warning.setForeground(Color.red);
                                 warning.setVisible(true);
                       }
                   }
                   else
                   {
                        warning.setText("Length of password must be greater than 8 ");
                        warning.setForeground(Color.red);
                        warning.setVisible(true);
                   }
                }
                else
                    
                {
                   warning.setText("Old Password is incorrect");
                   warning.setForeground(Color.red);
                   warning.setVisible(true);
                }
                
            }
    }//GEN-LAST:event_changepasswordActionPerformed

