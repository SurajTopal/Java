    void database(String email, String password) 
    {
         String url="jdbc:mysql://localhost:3306/demo";
        try
        {
           Class.forName("com.mysql.jdbc.Driver");
           Connection con=DriverManager.getConnection(url,"root","");
           String update_login="update demo.login set password='"+password+"' where email ='"+email+"'";
           String update_register="update demo.register set password='"+password+"' where email ='"+email+"'";
           Statement st=con.createStatement();
           int c=st.executeUpdate(update_login);
           int c1=st.executeUpdate(update_register);
           System.out.println(c+" updated in login , "+c1+" updated in register  ...");
           st.close();
           con.close();
        }
        catch(SQLException | NullPointerException|ClassNotFoundException e)
        {
          System.out.println(e);
        }
    }
    
