     public ArrayList<User> userlist()
     {
       ArrayList<User> userlist=new ArrayList<>();
        String url="jdbc:mysql://localhost:3306/demo"; 
        try
        {
          Class.forName("com.mysql.jdbc.Driver");
          String tablename=fetchTableName();
          Connection con=DriverManager.getConnection(url,"root","");
          String query="select * from demo."+tablename+"";
          Statement st=con.createStatement();
          ResultSet rs=st.executeQuery(query);
          User user;
          while(rs.next())
          {
             user=new User(rs.getString("transactionid"),rs.getString("title"),rs.getString("date"),rs.getString("time"),rs.getString("username"),rs.getInt("amount"));
             userlist.add(user);
          }
        }
        catch(ClassNotFoundException | SQLException e)
        {
          System.out.println("Exception  : "+e);
        }
        
        return userlist;
     }
         
