    public String fetchTableName()
    {
        String email=Login.email.getText();
        String accountno="";
        String url="jdbc:mysql://localhost:3306/demo";
        String select ="select accountno from demo.bankform where email='"+email+"'";
        try
      {      
          Class.forName("com.mysql.jdbc.Driver");
          Connection con=DriverManager.getConnection(url,"root","");
          Statement stmt=con.createStatement();
          ResultSet r=stmt.executeQuery(select);
          r.next();
          accountno=r.getString("accountno");
          System.out.println("Accountno : "+accountno);
      }
      catch(ClassNotFoundException | SQLException e)
      {
        System.out.println("Exception In Fetching Function : "+e);
      }
      return "user"+accountno;
    }
    
